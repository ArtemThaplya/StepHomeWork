package homeWork_6;

public class OneArray  {
    private int a;
    private int b;
    private int y;

    OneArray() {
        this.a = a;
        this.b = b;
        this.y = y;
    }


    public OneArray(int a, int b, int y) {
        this.a = a;
        this.b = b;
        this.y = y;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
